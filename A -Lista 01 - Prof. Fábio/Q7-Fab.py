# Entrada 

numero_1 = int (input ('Número 1: '))
numero_2 = int (input ('Número 2: '))
numero_3 = int (input ('Número 3: '))

# Processamento

soma = numero_1 + numero_2
diferença = numero_2 - numero_3

# Saída 

print (f'A soma dos dois primeiros números é igual a {soma} e a diferença entres os dois últimos é igual a {diferença}. ')